a = input('Type something')
print('The primitive type of this value is', type(a))
print('Is it only space?', a.isspace())
print('Is it only numbers?', a.isnumeric())
print('Is it only letters?', a.isalpha())
print('Is it alphanumeric?', a.isalnum())
print('Is it uppercase only?', a.isupper())
print('Is it lowercase only?', a.islower())
print('Is it capitalized?', a.istitle())




























